answer = input("""
    Enter Your Weather
    (Rainy, sunny or windy)
    """)
if answer == "rainy":
    print("It's raining. Take the umbrealla")
elif answer == "sunny":
    print("It's sunny. Stay hydrated")
elif answer == "windy":
    print("It's windy, Let's fly the kite")
else:
    print("Enter rainy, sunny, windy only")